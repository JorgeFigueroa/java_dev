    void TraerCategorias() {
        try {
            st = cone.createStatement();
            rs = st.executeQuery("select * from personal");

            Object datos[] = new Object[4];
            // limpiar tabla
            int fila = dtm.getRowCount();
            if (fila > 0) {
                for (int i = 0; i < fila; i++) {
                    dtm.removeRow(0);
                }
            }

            while (rs.next()) {
                datos[0] = rs.getInt(1); // guardando el dato en el array
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                
                dtm.addRow(datos); // adicionando los datos en el modelo de la tabla

            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }

